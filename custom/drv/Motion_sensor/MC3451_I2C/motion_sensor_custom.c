void mc3451_sensor_get_data(kal_uint16 *x_adc, kal_uint16 *y_adc, kal_uint16 *z_adc)
{
	kal_int16 x_temp,y_temp,z_temp;
	kal_int16 x,y,z;
	mc3451_read_xyz(&x_temp,&y_temp,&z_temp);
//	kal_prompt_trace(MOD_WAP,"Motion Sensor Raw value is raw_x : %d  ,raw_y : %d   ,raw_z : %d ",(signed char)x_temp,(signed char)y_temp,(signed char)z_temp);

#if defined MCUBE_CHIP_IS_MC3210
	//kal_prompt_trace(MOD_WAP,"Motion Sensor Raw value is raw_x : %d  ,raw_y : %d   ,raw_z : %d ,time_stamp = %d ",x_temp,y_temp,z_temp,time_ms);
	kal_prompt_trace(MOD_WAP,"Motion Sensor Raw value is raw_x : %d  ,raw_y : %d   ,raw_z : %d",x_temp,y_temp,z_temp);
	x=(kal_uint16)(x_temp+512);
	y=(kal_uint16)(y_temp+512);
	z=(kal_uint16)(z_temp+512);
#elif defined MCUBE_CHIP_IS_MC3433
	kal_prompt_trace(MOD_WAP,"Motion Sensor Raw value is raw_x : %d  ,raw_y : %d   ,raw_z : %d ,time_stamp = %d ",x_temp,y_temp,z_temp,time_ms);
	kal_prompt_trace(MOD_WAP,"Motion Sensor Raw value is raw_x : %d  ,raw_y : %d   ,raw_z : %d ",x_temp,y_temp,z_temp); 
	x=(kal_uint16)(x_temp+256);
	y=(kal_uint16)(y_temp+256);
	z=(kal_uint16)(z_temp+256);
#elif defined MCUBE_CHIP_IS_MC3451	
	x=(kal_uint16)(x_temp+1024);
	y=(kal_uint16)(y_temp+1024);
	z=(kal_uint16)(z_temp+1024);
#else
	 kal_prompt_trace(MOD_WAP,"Motion Sensor Raw value is raw_x : %d  ,raw_y : %d   ,raw_z : %d ,time_stamp = %d ",(signed char)x_temp,(signed char)y_temp,(signed char)z_temp,time_ms);
 
	 x_temp&=0xFF;                              
	 y_temp&=0xFF;
	 z_temp&=0xFF;
	 
	 //kal_prompt_trace(MOD_WAP,"Motion Sensor Raw value is raw_x1 : %d  ,raw_y1 : %d   ,raw_z1 : %d  ",x_temp,y_temp,z_temp);

	 if(x_temp<128)     
	     x=x_temp+127;
	 else
	 {
		 x=x_temp-128;
	 }
	 
	 if(y_temp<128)
	     y=y_temp+127;
	 else
	 {
		 y=y_temp-128;
	 }
	 
	 if(z_temp<128)
	     z=z_temp+127;
	 else
	 {
		 z=z_temp-128;
	 }
#endif 

// kal_prompt_trace(MOD_WAP,"Motion Sensor Raw value is raw_x2 : %d  ,raw_y2 : %d   ,raw_z2 : %d  ",x,y,z);
#if 1 //defined(MOTION_SENSOR_BACK_0)	
	*x_adc= x;
	*y_adc= y;
	*z_adc= z;
#elif defined(MOTION_SENSOR_BACK_90)
	*x_adc= y;
	*y_adc= 2*ACC_0G_X-x;
	*z_adc= z;
#elif defined(MOTION_SENSOR_BACK_180)
	*x_adc= 2*ACC_0G_X-x;
	*y_adc= 2*ACC_0G_Y-y;
	*z_adc= z;
#elif defined(MOTION_SENSOR_BACK_270)
	*x_adc= 2*ACC_0G_Y-y;
	*y_adc= x;
	*z_adc= z;
#elif defined(MOTION_SENSOR_FRONT_0)
	*x_adc= 2*ACC_0G_X-x;
	*y_adc= y;
	*z_adc= 2*ACC_0G_Z-z;
#elif defined(MOTION_SENSOR_FRONT_90)
	*x_adc= 2*ACC_0G_Y-y;
	*y_adc= 2*ACC_0G_X-x;
	*z_adc= 2*ACC_0G_Z-z;
#elif defined(MOTION_SENSOR_FRONT_180)
	*x_adc= x;
	*y_adc= 2*ACC_0G_Y-y;
	*z_adc= 2*ACC_0G_Z-z;
#elif defined(MOTION_SENSOR_FRONT_270)
	*x_adc= y;
	*y_adc= x;
	*z_adc= 2*ACC_0G_Z-z;
#endif

#ifdef MS_DBG
	 kal_prompt_trace(MOD_WAP,"Motion Sensor Readed and value is x:%d  ,y:%d   ,z:%d  ",*x_adc,*y_adc,*z_adc);
#endif
}

