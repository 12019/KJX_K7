void delay_ms(kal_uint32 millisecs)
{
	kal_uint32 start;
	volatile kal_uint32 duration, i;
	
	start = drv_get_current_time();  
	
	do
	{
	  for(i=0;i<5000;i++){}// loop 5000 times per 1ms
	  
	  duration = drv_get_duration_ms(start);
	  
	}while(duration < millisecs);

}
