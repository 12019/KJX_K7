void acc_sensor_init(void)
{
	//kal_uint8 ucBufferIndex;
	kal_uint8 temp[2];
	kal_uint8 check_sum;
	kal_uint16 chip_id,i;

	kal_prompt_trace(MOD_WAP,"acc_sensor_init xxxxxxxxxxxxxxxx");
	
#ifdef MS_DBG
	kal_prompt_trace(MOD_WAP,"mc3451_sensor_init start");
#endif

	 ms_i2c_configure(MS_SLAVE_ADDR, MS_DELAY_TIME); 

	delay_ms(100);
	  
	 //get chipid
	 for(i=0;i<10;i++)
	 {
	 	delay_ms(1);
		 mcube_i2c_read_buf(0x08,temp,2);
		 chip_id = (kal_uint16)(temp[0] << 8)|(kal_uint16)(temp[1]);
		 kal_prompt_trace(MOD_WAP,"motion_sensor_init 00 chip_id:%x",chip_id);

		 if((chip_id == 0x8128)||(chip_id == 0x000A))
		 	break;
	 }

	 if(chip_id == 0x8128)
	 {
	 	temp[0] = 0x5A;	//jump to OTP Bootloader mode
		MS_I2C_write_bytes(0x10,temp,1); 
	 }

	 delay_ms(30);

	  //get chipid
	 for(i=0;i<10;i++)
	 {
	 	 delay_ms(1);
		 mcube_i2c_read_buf(0x08,temp,2);
		 chip_id = (kal_uint16)(temp[0] << 8)|(kal_uint16)(temp[1]);
		 kal_prompt_trace(MOD_WAP,"motion_sensor_init 11 chip_id:%x",chip_id);
		
		 if(chip_id == 0x000A)
		 	break;
	 }
	 
	if(chip_id != 0x000A)return;

#ifdef MS_DBG
	kal_prompt_trace(MOD_WAP,"mc3451 version = %d",chip_id);    //chip_id=0x000A
#endif

	temp[0] = 0x00;
	temp[1] = 0x00;

	MS_I2C_write_bytes(0x01,temp,2); //set SRAM address point as 0x0000


#if 0
	for(i = 0; i < sizeof(sram_binxxxy)/2; i++)
	{	
		MS_I2C_write_bytes(0x02, sram_binxxxy+i*2, 2);		//download code		
	}
#else	//fast download block max for 8byte.
	for(i = 0; i < sizeof(sram_binxxxy)/4; i++)
	{	
		MS_I2C_write_bytes(0x02, sram_binxxxy+i*4, 4);			//download code		
	}
#endif

	check_sum = 0;
		
	for(i = 0; i < sizeof(sram_binxxxy); i++)
	{
		check_sum += sram_binxxxy[i];					//checksum
	}

	mcube_i2c_read_buf(0x10, temp, 1);

	if(check_sum != temp[0])  //check checksum
	{
#ifdef MS_DBG
		kal_prompt_trace(MOD_WAP,"mc3451 check sum err %d %d",check_sum,temp[0]);
#endif
		return;
	}

	temp[0] = 0x55;
	temp[1] = 0xAA;
	MS_I2C_write_bytes(0x04, temp, 2); //jump to SRAM OS

	delay_ms(100);
	
	for(i=0; i<10;i++)
	{
		mcube_i2c_read_buf(0x08,temp,2);
		chip_id = (kal_uint16)(temp[0] << 8)|(kal_uint16)(temp[1]);
		if(chip_id == 0x000A)
		{
			temp[0] = 0x55;
			temp[1] = 0xAA;
			MS_I2C_write_bytes(0x04, temp, 2); //jump to SRAM OS
		}
		else if(chip_id == 0x8128)
			break;

		delay_ms(10);
	}

	temp[0] = 0x01;
	MS_I2C_write_bytes(0x04, temp, 1); //enable pedometer // 0x03 -> reg:0x04  clean pedometer
	//LSB/g = 1024   +-8g    14bit	

#if 1
	//INIT SETTING
	temp[0] = MOTION_THM_VALUE;
	temp[1] = 0;
	MS_I2C_write_bytes(MOTION_THM, temp, 1); 
	temp[0] = MOTION_DUM_VALUE;
	temp[1] = 0;
	MS_I2C_write_bytes(MOTION_DUM, temp, 1); 
	temp[0] = MOTION_THS_VALUE;
	temp[1] = 0;
	MS_I2C_write_bytes(MOTION_THS , temp, 1); 
	temp[0] = MOTION_DUS_VALUE;
	temp[1] = 0;
	MS_I2C_write_bytes(MOTION_DUS, temp, 1); 
	
	temp[0] = STEPS_WAITING_VALUE;
	temp[1] = 0;
	MS_I2C_write_bytes(STEPS_WAITING, temp, 1); 

	temp[0] = THRETH_DYN_VALUE;
	temp[1] = 0;
	MS_I2C_write_bytes(THRETH_DYN , temp, 1); 

	temp[0] = MINCOUNT_CON_VALUE;  //  10 
	temp[1] = 0;
	MS_I2C_write_bytes(MINCOUNT_CON, temp, 1); 

	temp[0] = TIMEOUT_CON_VALUE;
	temp[1] = 0;
	MS_I2C_write_bytes(TIMEOUT_CON, temp, 1); 
#endif

	kal_prompt_trace(MOD_WAP,"motion_sensor_init ok");
}

