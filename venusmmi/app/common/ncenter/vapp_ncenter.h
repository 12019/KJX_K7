/***************************************************************************** 
 * Class VappNCenter 
 *****************************************************************************/

class VappNCenter : public VfxControl//, public VfxNObserver
{
    VFX_DECLARE_CLASS(VappNCenter);
    VFX_OBJ_DECLARE_SINGLETON_CLASS(VappNCenter);
// Constructor / Destructor
public:
    // Default constructor
    VappNCenter();   

private:
    enum
    {
         VAPP_NCENTER_TAB_NOTIFICATION,
         VAPP_NCENTER_TAB_ONGOING,       
         VAPP_NCENTER_TAB_TOTAL
    };

    
    enum
    {
        VAPP_NCENTER_TABBAR_TOP,
        VAPP_NCENTER_TABBAR_BOTTOM
    };


// Method
public:
    void enter();
    void leave();

    void requestLeave(VsrvNotification* noti);
    
     
    void disableLeaveOnScreenChange()
    {
        m_disableLeave = VFX_TRUE;
    }
    
    VfxBool getDisableLeaveOnScreeChange()
    {
        return m_disableLeave;
    }

    void onRotate(VfxScreenRotateParam param);

    VfxBool getDisableDrag()
    {
        return m_disableDrag;
    }

    void setDisableDrag(VfxBool value)
    {
        m_disableDrag = value;
    }

    void setNCenterScr(VappNCenterScr *nc)
    {
        m_ncenterScrPtr = nc;
    }

    VappNCenterScr* getNCenterScr()
    {
        return m_ncenterScrPtr.get();
    }

    VfxBool getNCenterEntered()
    {
        return m_entered;        
    }

    VfxBool initAsmMemory();
    void deinitAsmMemory();    


    void emitEvent(VappNCenterEventEnum event);
    
            
// Overridable
public:
    // IVfxAllocatable interfaces
    virtual vrt_allocator_handle getAllocator();



// Override
protected:
    virtual void onInit();
    virtual VfxBool onPenInput(VfxPenEvent &event);
    virtual VfxBool onKeyInput(VfxKeyEvent &event);

    virtual void setBounds(const VfxRect &value);

    
    void onTabSwitched(VfxObject* tabBar, VfxId tid);
    void onCloseContentView(VfxId vid);
    void onCreateContentView(VfxId vid, VfxFrame* parent);
    
    VfxS32 onCreateContentTop(VfxFrame* parent);
    VfxS32 onCreateContentBottom(VfxFrame* parent); 


    
    void onPopupButtonClicked(VfxObject* sender, VfxId id);
    
// Signal
public:
    VfxSignal1 <VappNCenterEventEnum>  m_signalEvent;
    
// Implementation
private:
    void onRequestUpdate(VfxId id,VfxS32 height);
    void onPopupChanged(VfxWeakPtr<VfxBasePopup> popup);
    
    void onEnterEnd(VfxTimer *source);

    void onEventClearAll(VfxObject* sender, VfxId id);
    

    void createSystemView();
    void releaseSystemView();


    
    void onActiveScreenEnter(VfxWeakPtr <VfxScreen> scr);
    void onActiveScreenExit(VfxWeakPtr <VfxScreen> scr);
    
    void leaveInternal();
    void leaveInternalEx(VfxTimer *source);   


    void createNoTextFrame();
    void createClearCell();

    void onNotificationChanged(VsrvNotification* noti, VsrvNStatusType status);
    VfxBool checkNotification(VsrvNotification *noti);
    VfxBool checkIsTheSameType(VsrvNotification *noti, VappNCenterListCellStruct* data);


    VfxS32 createTabBar(VfxFrame* parent);
    VfxBool createCell(VsrvNotification *noti, VfxId refId, VfxBool addBeforeRef = VFX_FALSE);
    VfxBool closeCell(VsrvNotification *noti);

    void updatePanelPos(VfxPenEvent &event);

private:
    VcpStatusIconBar *m_statusBar;



    VfxContext *m_context;
    VfxContext *m_uiContext;
    VfxU8 *m_memPtr;
    MMI_ID m_asmId;    
    
    VsrvNotification* m_callbackNoti; 

    VfxTextFrame *m_noTextFrame;
    VfxS32 m_eventCount;
    VfxS32 m_ongoingCount;    

    VappNCenterList *m_list;

    VfxFrame *m_panel;
    VfxTimer *m_enterTimer;
    VfxTimer *m_leaveTimer;
    VfxFrame *m_bottom;
    VfxImageFrame *m_bottomImage;

    VfxS32 m_originY;
    VfxS32 m_topHeight;
    VfxFrame* m_panelContent;   
    VfxFrame* m_topRegion;      
    VfxFrame* m_bottomRegion;   

    VfxId m_currentViewId;
    VfxRect m_formBounds[VAPP_NCENTER_TAB_TOTAL];

        
    VfxS32 m_tabBarPos; 
    VfxS32 m_bottomHeight;  
    

    VappNCenterOperatorCell *m_operatorCell;
    VappQuickSettingCell *m_quickSettingCell;
    VappNCenterClearCell *m_clearCell;
#if defined(__MMI_SAFE_MODE__)    
    VappSafeModeNotificationCell *m_safeModeCell; 
#endif    
    VfxBool m_operatorCellIsFold;
    
    VfxBool m_disableLeave;
    VfxBool m_disableDrag;

    
    VcpTabBar *m_tabBar;

    
    VfxWeakPtr <VappNCenterScr> m_ncenterScrPtr;
    VfxBool m_entered;
    VfxBool m_isDrag;

    friend class VappNCenterScr;
};


