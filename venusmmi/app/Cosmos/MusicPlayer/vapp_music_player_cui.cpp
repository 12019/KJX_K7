mmi_ret VcuiMusicPlayer::onProc(mmi_event_struct *evt)
{
    MMI_TRACE(TRACE_GROUP_2, TRC_VAPP_MUSICPLY_VCUI_ON_PROC, evt->evt_id);
    
    switch(evt->evt_id)
    {
        case EVT_ID_VAPP_SCREEN_LOCK_PRE_LOCK_EVT_ROUTING:
        {
            const vapp_screen_lock_pre_lock_evt_struct *lockEvt = reinterpret_cast<vapp_screen_lock_pre_lock_evt_struct*>(evt);
            const PlaybackStateEnum state = m_ctrlPlayback->getPlayState();

            if (lockEvt->mode == VAPP_SCREEN_LOCK_MODE_BY_POWER_KEY)
            {
                if (state == PB_STATE_PLAY || state == PB_STATE_BT_CONNECTING_WHILE_PLAYING)
                {
                    if (getNowPlayingPage())
                    {
                        if(m_ctrlPlayback->isSongPausable(NULL))
                        {
                            getNowPlayingPage()->pause();
                        }
                        else
                        {
                            getNowPlayingPage()->stop();
                        }
                    }
                }
                return MMI_RET_OK;
            }
            else if (lockEvt->mode == VAPP_SCREEN_LOCK_MODE_BY_AUTO_LOCK)
            {
                if (state == PB_STATE_PLAY || state == PB_STATE_BT_CONNECTING_WHILE_PLAYING)
                {
                    return MMI_RET_ERR;
                }
                return MMI_RET_OK;
            }

            break;
        }

        case EVT_ID_SRV_FMGR_NOTIFICATION_DEV_PLUG_OUT:
        {
            if (m_isSongInCard)
            {
                mmi_group_event_struct evt;
                MMI_FRM_INIT_GROUP_EVENT(&evt, EVT_ID_VCUI_MUSIC_PLAYER_CLOSE, m_cuiId);
                postToCaller((mmi_group_event_struct*)&evt);
            }
            break;
        }
        case EVT_ID_SRV_UCM_STATUS_CHANGE:
        {
            if (srv_ucm_query_call_count(SRV_UCM_CALL_STATE_ALL, SRV_UCM_CALL_TYPE_NO_CSD, NULL) > 0)
            {
                /* Disable all UI in call */
                if (getNowPlayingPage())
                {
                    getNowPlayingPage()->setEnablePlaybackUI(VFX_FALSE);
                }
            }
            else
            {
                if(m_isFileValid)
                {
                    if(m_ctrlPlayback->getPlayState() == PB_STATE_NONE)
                    {
                        m_scr->open();
                    }

                    /* Enable UI end call */
                    if (getNowPlayingPage())
                    {
                        getNowPlayingPage()->setEnablePlaybackUI(VFX_TRUE);
                        getNowPlayingPage()->setHittablePlaybackUI(VFX_TRUE);
                    }
                }
            }

            break;
        }
        case EVT_ID_VOL_DIALOG_CHECK_VOL_TYPE:      
        {
            vapp_vol_dialog_evt_struct *volEvent = (vapp_vol_dialog_evt_struct*) evt;

            if (volEvent->type == VAPP_VOL_DIALOG_IS_MEDIA)
            {
                const VfxBool isTopActive = isGroupTopActive();
                if (isTopActive)
                {
                    return !MMI_RET_OK;
                }
                else
                {
                    return MMI_RET_OK;
                }
            }
            break;
        }
        case EVT_ID_SRV_PROF_IS_PLAYING:
        {
            const PlaybackStateEnum state = m_ctrlPlayback->getPlayState();

            if (state == PB_STATE_PLAY || state == PB_STATE_BT_CONNECTING_WHILE_PLAYING)
            {
                return !MMI_RET_OK;
            }
            else
            {
                return MMI_RET_OK;
            }
            break;
        }
    }

    return VfxCui::onProc(evt);
}

void VcuiMusicPlayer::postProcess()
{
    // close music player
    MMI_ID instance_id = srv_appmgr_query_instance_single_id("native.mtk.musicplayer");
    srv_appmgr_query_instance_id("native.mtk.musicplayer", &instance_id, 1);
    srv_appmgr_close_app(instance_id);

    // show CUI screen
    m_scr->show();
}

