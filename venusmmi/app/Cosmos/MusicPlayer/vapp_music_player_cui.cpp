U8 VcuiMusicPlayer::onCuiAVRCPCommand(U8 command_type, srv_avrcp_key_events key_events)
{
    // we only handle "down" events, don't handle
    // long-press, repeat, and up.
    if(SRV_AVRCP_KEY_DOWN != key_events)
    {
        return SRV_AVRCP_CR_NOT_IMPLEMENT;
    }

    MMI_TRACE(TRACE_GROUP_2, TRC_VAPP_MUSICPLY_VCUI_ON_AVRCP_COMMAND, command_type, key_events);

    // Only act AVRCP command for active screen
    if (!isGroupActive())
    {
        return SRV_AVRCP_CR_ACCEPT;
    }
    
    VappMusicPlayerNowPlayingPage* nowPlaying = getNowPlayingPage();
    VFX_ASSERT(nowPlaying);

    switch(command_type)
    {
        case SRV_AVRCP_POP_PLAY:
        case SRV_AVRCP_POP_PAUSE:
            // for BT AVRCP, we always treat play/pause as "toggle",
            // this is the behavior of Pluto MMI media player.
            nowPlaying->playpause();
            return SRV_AVRCP_CR_ACCEPT;

        case SRV_AVRCP_POP_STOP:
            nowPlaying->stop();
            nowPlaying->updateTimeInfo();
            return SRV_AVRCP_CR_ACCEPT;

        default:
            return SRV_AVRCP_CR_NOT_IMPLEMENT;
    }

    return SRV_AVRCP_CR_NOT_IMPLEMENT;
}
