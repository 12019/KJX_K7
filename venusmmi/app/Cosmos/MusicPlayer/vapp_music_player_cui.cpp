void VcuiMusicPlayer::onShowHideWaitIcon(VfxBool isShow, VfxBool delayShow)
{
    if (isShow)
    {
        showWaitIcon(delayShow);
    }
    else
    {
        hideWaitIcon();
    }
}

void VcuiMusicPlayer::showWaitIcon(VfxBool delayShow)
{
    if (delayShow)
    {
        if (m_waitIconTimer.get() == NULL)
        {
            VFX_OBJ_CREATE(m_waitIconTimer, VfxTimer, this);
            m_waitIconTimer->setStartDelay(300);
            m_waitIconTimer->m_signalTick.connect(this, &VcuiMusicPlayer::doShowWaitIcon);
        }
        m_waitIconTimer->start();
    }
    else
    {
        doShowWaitIcon(NULL);
    }
}

void VcuiMusicPlayer::doShowWaitIcon(VfxTimer *timer)
{
    if (m_waitIcon)
    {
        m_waitIcon->bringToFront();
        m_waitIcon->setHidden(VFX_FALSE);
        m_waitIcon->start();
    }
}

void VcuiMusicPlayer::hideWaitIcon()
{
    if (m_waitIconTimer.get())
    {
        m_waitIconTimer->stop();
    }

    if (m_waitIcon)
    {
        if (m_waitIcon->isAnimating())
        {
            m_waitIcon->stop();
        }

        m_waitIcon->setHidden(VFX_TRUE);
        m_waitIcon->sendToBack();
    }
}

void VcuiMusicPlayer::updateLayout(VfxBool isPortrait)
{
    VfxU32 width = isPortrait ? GDI_LCD_WIDTH : GDI_LCD_HEIGHT;
    VfxU32 height = isPortrait ? GDI_LCD_HEIGHT : GDI_LCD_WIDTH;

    m_waitIcon->setPos(width/2, (height - MMI_STATUS_BAR_HEIGHT)/2);    
}

void VcuiMusicPlayer::onMDICallback(VfxS32 result)
{
    MMI_TRACE(TRACE_GROUP_2, TRC_VAPP_MUSICPLY_VCUI_ON_MDI_CALLBACK, result);
    
    VappMusicPlayerNowPlayingPage* nowPlaying = getNowPlayingPage();
    if (nowPlaying)
    {
        switch (result)
        {
            // For playback
            case VAPP_MUSICPLY_PRE_END_OF_FILE:
                if (!nowPlaying->m_sliderDragging)
                {
                    nowPlaying->maximizeSlider();
                }                    
                break;
                
            case VAPP_MUSICPLY_EOF_ON_PLAY_REQ:
            case MDI_AUDIO_END_OF_FILE:
                {
                    if (!nowPlaying->m_sliderDragging)
                    {
                        mmi_group_event_struct evt;
                        MMI_FRM_INIT_GROUP_EVENT(&evt, EVT_ID_VCUI_MUSIC_PLAYER_PLAY_FINISH, m_cuiId);
                        postToCaller((mmi_group_event_struct*)&evt);

                        nowPlaying->stop();
                        nowPlaying->resetSlider();
                        // Consume DRM when replay after EOF
                        nowPlaying->setConsumeDRM(VFX_TRUE);
                    }
                }
                break;

            case MDI_AUDIO_TERMINATED:
                break;

            // For duration and seek
            case MDI_AUDIO_DUR_UPDATED:
                nowPlaying->MDICallbackHandler(result);
                break;

            case AUD_SEEK_REASON_DONE:
                {
                    if(!m_ctrlPlayback->seek(m_ctrlPlayback->m_seekTime))
                    {
                        return;
                    }
                    m_ctrlPlayback->updateDuration(VFX_TRUE);
                    nowPlaying->updateDuration();
                    
                    PlaybackStateEnum state = m_ctrlPlayback->m_playStateBeforeSeek;

                    if (state == PB_STATE_PLAY)
                    {
                        // Not update slider value when play afer seek
                        nowPlaying->play(VFX_WSTR(""), VFX_TRUE, VFX_FALSE);
                    }
                    else if (state == PB_STATE_PAUSE
                          || state == PB_STATE_STOP
                          || state == PB_STATE_INTERRUPTED
                          || state == PB_STATE_OPEN)
                    {
                        m_ctrlPlayback->setPlayState(state);
                    }
                    else
                    {
                        m_ctrlPlayback->stop();
                    }
                }
                break;

            case AUD_SEEK_REASON_FAIL:
            case AUD_SEEK_REASON_TERMINATED:
                nowPlaying->MDICallbackHandler(result);
                break;

            // For interrupt
            case VAPP_MUSICPLY_INTERRUPT_CB_INTERRUPT:

#if defined(__MMI_AVRCP_SUPPORT__)
				// unregister AVRCP
				srv_avrcp_clear_cmd_hdlr(onCuiAVRCPCmdCallback);
#endif
                break;

            case VAPP_MUSICPLY_INTERRUPT_CB_RESUME:
                nowPlaying->updateTimeInfo();
                break;

            // For playback error
            default:
                {
                    nowPlaying->stop();
                    nowPlaying->closeFile();
                    nowPlaying->resetSlider();
                    nowPlaying->setEnablePlaybackUI(VFX_FALSE);
                    m_isFileValid = VFX_FALSE;

                    VfxResId strID = m_ctrlPlayback->getMDIErrorMessage(result);
                    nowPlaying->showErrorMessage(VFX_WSTR_RES(strID), VFX_FALSE);

                    // Disconnect the playback state change signal, so that playback UI will not be enabled again by interruption
                    m_ctrlPlayback->m_signalPlaybackStateChange.disconnect(nowPlaying, &VappMusicPlayerPlayerBase::onPlaybackStateChange);
                }   
                break;
        };
    }
}

