void VcuiMusicPlayerScreen::setFilePath(VfxWString &filePath)
{
    m_filePath = filePath;
    m_nowPlaying->setAlbumCover(0, filePath, PAGE_SWITCH_DIR_NONE);
}

void VcuiMusicPlayerScreen::setTitle(VfxWString &title, VfxWString &subtitle)
{
    m_title = title;
    m_subtitle = subtitle;

    VfxWString emptyStr;
    emptyStr.format("");

    if (m_nowPlaying)
    {
        // try to parse info from file
        srv_plst_media_details_struct *info = m_nowPlaying->getAudioInfo();
        if (info && vfx_sys_wcslen(info->title))
        {
            if (info->title)
            {
                m_title.loadFromMem(info->title);
            }
            else
            {
                m_title.loadFromRes(STR_ID_VAPP_MUSIC_PLAYER_UNKNOWN_SONG);
            }
            
            if (info->artist)
            {
                m_subtitle.loadFromMem(info->artist);
            }
            else
            {
                m_title.loadFromRes(STR_ID_VAPP_MUSIC_PLAYER_UNKNOWN_ARTIST);
            }

            m_nowPlaying->setInfoText(m_title, m_subtitle, emptyStr);
        }
        else
        {
            m_nowPlaying->setInfoText(m_title, m_subtitle, emptyStr);
        }
    }
}

void VcuiMusicPlayerScreen::open()
{
    if (!m_filePath.isEmpty())
    {
        m_nowPlaying->open(m_filePath);
    }
}

void VcuiMusicPlayerScreen::play()
{
    if (srv_ucm_query_call_count(SRV_UCM_CALL_STATE_ALL, SRV_UCM_CALL_TYPE_NO_CSD, NULL) > 0)
    {
        /* Disable UI */
        m_nowPlaying->setEnablePlaybackUI(VFX_FALSE);

        /* Inform user */
        mmi_frm_nmgr_balloon(
            MMI_SCENARIO_ID_DEFAULT,
            MMI_EVENT_INFO_BALLOON,
            MMI_NMGR_BALLOON_TYPE_FAILURE,
            (WCHAR *)VFX_WSTR_RES(STR_GLOBAL_CURRENTLY_NOT_AVAILABLE_IN_CALL).getBuf());

        return;
    }

    if (!m_filePath.isEmpty())
    {
        m_nowPlaying->play(m_filePath);
    }
}

VappMusicPlayerNowPlayingPage* VcuiMusicPlayerScreen::getNowPlaying()
{
    return m_nowPlaying;
}

void VcuiMusicPlayerScreen::clearNowPlayingPtr(VappMusicPlayerNowPlayingPage *nowPlaying)
{
    if (nowPlaying == m_nowPlaying)
    {
        m_nowPlaying = NULL;
    }
}

void VcuiMusicPlayerScreen::onNowPlayingCallabck(VfxS32 action)
{
    switch(VappMusicPlayerPlayerPageCallbackEnum(action))
    {
        case FUNC_ID_PAGE_ENTERED:
            // Move to on2ndReady since FW will call onEnter() and onEntered() at the same time when switch screen.
            /*if (m_firstEnter)
            {
                m_firstEnter = VFX_FALSE;
                play();
            }*/
            break;

        case FUNC_ID_BTN_PLAY_PAUSE:
        {
            VcuiMusicPlayer *vcui = (VcuiMusicPlayer*)getApp();
        
            // If state is not NONE, playerbase will handle it
            if((vcui != NULL) && (vcui->m_ctrlPlayback != NULL))
            {
                const PlaybackStateEnum state = vcui->m_ctrlPlayback->getPlayState();
                if(state == PB_STATE_NONE)
                {
                    play();
                }
                else if(state == PB_STATE_CLOSE)
                {
                    /* Set file path only, the playerbase module will handle the play request */
                    if (!m_filePath.isEmpty())
                    {
                        vcui->m_ctrlPlayback->setFilePath(m_filePath);
                    }
                }
            }
            break;
        }
        default:
            break;
    }
}


