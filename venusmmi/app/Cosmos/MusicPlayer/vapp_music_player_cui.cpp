void VcuiMusicPlayer::onRun(void * args, VfxU32 argSize)
{
    MMI_TRACE(TRACE_GROUP_2, TRC_VAPP_MUSICPLY_VCUI_ON_RUN);
    
    VfxApp::onRun(args, argSize);

    m_isFileValid = VFX_TRUE;
    g_cui_this_ptr = this;
    
    VFX_OBJ_CREATE(m_ctrlPlayback, VappMusicPlayerCtrlPlayback, this);
    m_ctrlPlayback->m_signalMDICallback.connect(this, &VcuiMusicPlayer::onMDICallback);
    m_ctrlPlayback->m_signalShowHideWaitIcon.connect(this, &VcuiMusicPlayer::onShowHideWaitIcon);
    m_ctrlPlayback->setIsAppMode(VFX_FALSE);

    // terminate background play, since playing a song in file manager is not an interrupting action
    m_ctrlPlayback->terminateBackgroundPlay();

#if defined(__MMI_AVRCP_SUPPORT__)
    // register AVRCP
    srv_avrcp_set_cmd_hdlr(onCuiAVRCPCmdCallback);
#endif

    // create and display main screen
    VFX_OBJ_CREATE(m_scr, VcuiMusicPlayerScreen, this);

    // create wait icon
    VFX_OBJ_CREATE(m_waitIcon, VcpActivityIndicator, m_scr);
    m_waitIcon->setAnchor(0.5, 0.5);
    m_waitIcon->setHiddenWhenStopped(VFX_TRUE);
    m_waitIcon->setPos(GDI_LCD_WIDTH/2, (GDI_LCD_HEIGHT - MMI_STATUS_BAR_HEIGHT)/2);
    m_waitIcon->stop();

    m_scr->setCtrlPlayback(m_ctrlPlayback);

    VfxWChar *filePath;
    VFX_ALLOC_MEM(filePath, argSize, this);
    memcpy(filePath, args, argSize);
    VfxWString str;
    str.loadFromMem(filePath);
    m_scr->setFilePath(str);
    
    VfxWString driveLetter;
    driveLetter.loadFromMem(srv_fmgr_drv_get_path_by_type(SRV_FMGR_DRV_CARD_TYPE));

    m_isSongInCard = vfx_sys_wcsncpy(filePath, const_cast<VfxWChar*>(driveLetter.getBuf()), driveLetter.getLength());

    VFX_FREE_MEM(filePath);

    registerCBEvent(EVT_ID_VAPP_SCREEN_LOCK_PRE_LOCK_EVT_ROUTING, VFX_TRUE);
    registerCBEvent(EVT_ID_SRV_FMGR_NOTIFICATION_DEV_PLUG_OUT, VFX_TRUE);
    registerCBEvent(EVT_ID_SRV_UCM_STATUS_CHANGE, VFX_TRUE);
    registerCBEvent(EVT_ID_VOL_DIALOG_CHECK_VOL_TYPE, VFX_TRUE);
    registerCBEvent(EVT_ID_SRV_PROF_IS_PLAYING, VFX_TRUE);

	VappNCenter *ncenter =  VFX_OBJ_GET_INSTANCE(VappNCenter);
	if(ncenter != NULL)
	{
		ncenter->m_signalEvent.connect(this, &VcuiMusicPlayer::onNCenterStatusChanged);
	}


    vfxPostInvoke0(this, &VcuiMusicPlayer::postProcess);
}

