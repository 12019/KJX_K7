#ifndef __VAPP_SCREEN_LOCK_MAIN_H__
#define __VAPP_SCREEN_LOCK_MAIN_H__


/***************************************************************************** 
 * Include
 *****************************************************************************/
#include "vfx_mc_include.h"
#include "vcp_include.h"

/***************************************************************************** 
 * Define
 *****************************************************************************/

typedef enum
{

    /*
     * This event is used to post launch screen lock
     *
     */
    EVT_ID_VAPP_SCREEN_LOCK_LAUNCH_REQ,

    EVT_ID_LOCAL_END_OF_ENUM
} vapp_screen_lock_local_event_enum;


/***************************************************************************** 
 * Typedef
 *****************************************************************************/


/***************************************************************************** 
 * Class 
 *****************************************************************************/
class VappScreenLockApp : public VfxApp
{
    VFX_DECLARE_CLASS(VappScreenLockApp);

// Override
protected:
    virtual void onInit();
    virtual void onDeinit();
    virtual void onRun(void* args, VfxU32 argSize);
    virtual void onGroupActive();
    virtual void onGroupInactive();
    virtual void onGroupSnapshotActive();
    virtual void onGroupSnapshotInactive();    

    // Home key
    // When user press home key, FW will trigger "back to Idle" process.
    // Every application will be asked for processing "close" with available choices via VfxApp::onProcessClose().
    virtual VfxAppCloseAnswerEnum onProcessClose(VfxAppCloseOption options);

// Signal
public:
    // This signal is emitted when the screen lock application is active. Internal
    // objects of the screen lock applications can connect this signal.
    VfxSignal0 m_signalActive;

    // This signal is emitted when the screen lock application is inactive. Internal
    // objects of the screen lock applications can connect this signal.
    VfxSignal0 m_signalInactive;

    // This signal is emitted when application is activated due to start of small screen redrawing
    VfxSignal0 m_signalSnapshotActive;
    
    // This signal is emitted when application is inactivated due to start of small screen redrawing    
    VfxSignal0 m_signalSnapshotInactive;    

// Method
public:
    mmi_id getScreenId();

private:
    mmi_id m_screenId;
};
#endif /* __VAPP_SCREEN_LOCK_MAIN_H__ */
