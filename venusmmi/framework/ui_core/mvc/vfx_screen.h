/***************************************************************************** 
 * Class VfxScreen
 *****************************************************************************/

/*
 * VfxScreen is the base class of a screen.
 */ 
class VfxScreen : public VfxControl
{
    VFX_DECLARE_CLASS(VfxScreen);    

// Constructor / Destructor
public:
    // Default Constructor.
    VfxScreen();

// Method
public:
    // Set the VRT Cache size in bytes. 
    //  Default value is VFX_SYS_MEM_VRT_CACHE_MAX_SIZE.
    //  This value should be set before active, and it will apply on the 
    //  screen become active.
    void setVrtCacheSize(
        VfxU32 size             // [IN] The VRT cache value to set
    );

    // Get the VRT Cache size in bytes. 
    VfxU32 getVrtCacheSize() const;

    // Set the VRT cache limit size for setVrtCacheSize()
    void setVrtCacheLimitSize(
        VfxU32 size             // [IN] The VRT cache limit value to set
    );

    // Return this screen whatever a small screen.
    //
    // RETURNS: The screen whatever a small screen.
    VfxBool getIsSmallScreen() const;

    // Switch this screen to a full screen from small
    void disableSmallScreen();

    // Return this screen whether disable small screen 
    // When the screen have switch to full screen from small,
    // it would be diable small screen.
    VfxBool getIsSmallScreenDisable() const;

    VfxBool getIsSnapshotDrawing() const;

    // Register the access key to control.
    // 
    // SEE ALSO: registerKeyHandler
    void registerAccessKeyHandler(VfxKeyCodeEnum keyCode, VfxControl *control);

    // Unregister the access key to control.
    // 
    // SEE ALSO: unregisterKeyHandler
    void unregisterAccessKeyHandler(VfxKeyCodeEnum keyCode, VfxControl *control);
        
    // Unregister all the access key about a control
    // 
    // SEE ALSO: unregisterKeyHandler
    void unregisterAllAccessKeyHandler(VfxControl *control);

// Framework method
public:
    // Enters venus canvas engine.
    static void entryCanvas(
        VfxContext *context,                   // [IN] Passes a context for canvas engine.
        const VfxScreenEntryCanvasParam &param // [IN] Entry Canvas parameters
    );

    // Enters venus canvas engine with default parameters.
    static void entryCanvas(
        VfxContext *context // [IN] Passes a context for canvas engine.
    );

    // Leaves veuns canvas engine.
    static void leaveCanvas(
        const VfxScreenLeaveCanvasParam &param // [IN] Leave Canvas parameters
    );

    // Leaves veuns canvas engine with default parameters.
    static void leaveCanvas();

// Method
public:
    // Return the current client area rectangle
    VfxRect getClientRect() const
    {
        return onGetClientRect();
    }

    // Return the active popup of the screen
    VfxBasePopup *getActivePopup() const
    {
        return (VfxBasePopup *)m_popupStack.getFirst();
    }

    // Destroy all popup below to this screen
    void clearPopupStack();

    // check if any popup present
    VfxBool isPopupPresent() const
    {
        return m_screenDisable;
    }

    // A hint that the next screen is also a Venus screen, 
    //  and UI engine can enhance some performance when leaving and entering.
    void setNextIsVenus();

    // Set APP context heap whatever share for VRT context heap
    void setAppContextShareForVrt(VfxBool isEnable);

    // Set the screen rotate has no content animation
    void setNoAnimRotate(VfxBool value);

    VfxBool isNoAnimRotate();

// Property
public:
    // Set transit type
    void setTransitType(VfxScrTransitTypeEnum type);

    // Query current transit type
    VfxScrTransitTypeEnum getTransitType() const;

// Method
protected:
    // Set the screen rotate type
    void setScreenRotateType(
        VfxScrRotateTypeEnum newRotateType // [IN] The new screen rotate type to set
    );

    void setIsCustomRotate(VfxBool value);

    VfxBool isCustomRotate() const;

// Overridable
protected:
    friend class VfxTopLevel;

    // Override this virtual function to return the current client area rectangle.
    //  The defualt is return the bounds of the screen.
    virtual VfxRect onGetClientRect() const;

    // While entering the screen to be active
    virtual void onEnter(VfxBool isBackward);
    
    // While screen to be active and after it displayed
    virtual void onEntered(VfxBool isBackward);
    
    // While leaving the screen to be inactive
    virtual void onExit(VfxBool isBackward);

    // while exit the screen
    virtual void onExited(VfxBool isBackward);

    // While entering the screen to be snapshot
    virtual void onSnapshotEnter();
    
    // While leaving the screen to be snapshot
    virtual void onSnapshotExit();

    // Override this virtual function to change the screen support rotate types.
    //  The defualt behavior is setting param.rotateTo to VFX_SCREEN_SUPPORT_ROTATE_NORMAL
    virtual void onQueryRotateEx(
        VfxScreenRotateParam &param // The parameter to rotate
    );

    // While the LCD screen has been rotate
    virtual void onRotate(
        const VfxScreenRotateParam &param // The parameter to rotate
    );

// Implementation
private:
	static void onEnteredCallback(void *screenHandle);

// Overridable
public:
    // While IME becomes active, it notifies screen to adjust layout to fit input area
    virtual void setIMElayout(
        VfxRect &imeRect,       // [IN] IME area
        VfxFrame *imeOwner      // [IN] The owner who creates IME
    );

    // While IME becomes inactive, it notifies to reset the layout
    virtual void resetIMElayout();

/* DOM-NOT_FOR_SDK-BEGIN */
// Method
public: 
    // Internal used. 
    // Return the current screen rotate type
    VfxScrRotateTypeEnum getRotateType() const
    {
        return m_rotateType;
    }

    // Internal used. 
    // Return the current screen rotate type
    void setRotateType(VfxScrRotateTypeEnum type)
    {
        m_rotateType = type;
    }

    // Internal used. 
    //  Before enter this screen. Setup Canvas Engine (VRT)
    static void processBeforeEnter(VfxScreen *screen, VfxBool isBackward);
    // Internal used. 
    //  Enter this screen. Callback for sub class to setup screen.
    virtual void processEnter(VfxBool isBackward);
    // Internal used. 
    //  After enter this screen. Commit scene graph and try to resume VRT
    static void processAfterEnter(VfxScreen *screen, VfxBool isBackward);
    // Internal used. 
    //  After screen scene graph commit.
    virtual void processEntered(VfxBool isBackward);

    // Internal used. Before leave this screen
    void processBeforeExit(VfxBool isBackward);
    // Internal used. Leave this screen
    virtual void processExit(VfxBool isBackward);
    // Internal used. After exit this screen
    virtual void processAfterExit(VfxBool isBackward);

    // Internal used. 
    virtual void queryRotate(VfxScreenRotateParam &param);
    // Internal used. 
    virtual void processRotate(const VfxScreenRotateParam &param);

    // Internal used. 
    //  This method is called by popup when show a popup.
    void showPopup(
        VfxBasePopup *popup,        // [IN] current popup
        VfxBool isAnim              // [IN] show animation or not
    );
    // Internal used. 
    //  This method is called by popup when a popup is leaving.
    void leavePopup(
        VfxBasePopup *popup,        // [IN] current popup
        VfxBool isAnim              // [IN] show animation or not
    );
/* DOM-NOT_FOR_SDK-END */

// Override
protected:
    virtual void onInit();
    virtual void onDeinit();

    void onFocusChanged(VfxBool value);
    virtual void onObjectNotify(VfxId eventId, void *userData);
    
    virtual void processRegisterAccessKeyHandler(VfxKeyCodeEnum keyCode, VfxControl *target);
    virtual void processUnregisterAccessKeyHandler(VfxKeyCodeEnum keyCode, VfxControl *target);
    virtual void processUnregisterAllAccessKeyHandler(VfxControl *target);
    virtual VfxBool processFocusKey(VfxKeyEvent &event);

// Variable
protected:
    // Access key manager.
    VfxAccessKeyMgr     *m_pAccessKeyMgr;    

    // Get context of the screen
    VfxContext *getContext() const;

    // Switch this screen to a small screen.
    // This method can only called on constructor.
    void setIsSmallScreen();

    // Set the screen is in snaptshot drawing mode, ex. small screen redraw
    void setIsSnapshotDrawing(VfxBool value);

    // Internal used. 
    virtual void onScreenDisabled(VfxBool isDisabled){}

// Implementation
private:
    // Find context of the screen
    VfxContext *findContext();

    void onBlackScreenTimerStop(VfxTimer *source);
    void onEnteredTimerTick(VfxTimer *source);
        
    void switchToEnable();
    void switchToDisable(VfxFrame *frame, VfxBool isAnim = VFX_TRUE, VfxBool isStatic = VFX_FALSE);

    void suspendPopupStack();
    void resumePopupStack();

    enum
    {
        SCREEN_FLAGS_NONE               = 0,
        // The screen is not full of screen
        SCREEN_FLAGS_SMALL_SCREEN       = (1 << 1),
        // The screen is for freeze drawing
        SCREEN_FLAGS_SNAPSHOT_DRAWING   = (1 << 2),
        // See method setNextIsVenus()
        SCREEN_FLAGS_NEXT_IS_VENUS      = (1 << 3),
        // The screen is backward this time
        SCREEN_FLAGS_BACKWARD_ENTERED   = (1 << 4),
        // The screen is in enter process
        SCREEN_FLAGS_ENTERING           = (1 << 5),
        // The screen is in enter process
        SCREEN_FLAGS_CUSTOM_ROTATE      = (1 << 6),
        // Set APP context heap whatever share for VRT context heap
        SCREEN_FLAGS_APP_CONTEXT_SHARE_FOR_VRT = (1 << 7),
        // Screen rotate has no content animation
        SCREEN_FLAGS_NO_ANIM_ROTATE     = (1 << 8),
        // The screen has switched to full screen from small
        SCREEN_FLAGS_SMALL_SCREEN_DISABLE = (1 << 9)
    };

    // The VRT cache size of the screen
    VfxU32      m_vrtCacheSize;

    // VRT cache limit size of the screen
    VfxU32      m_vrtCacheLimitSize;

    // Screen flags.
    VfxFlag     m_screenFlags;

    // Owner context
    VfxContext *m_pContext;
    
    /*
     * IME  
     */
    VfxBool     m_isStartIme;
    VfxRect     m_rectImeTarget;
    VfxWeakPtr <VfxFrame> m_frameImeTarget;
    VfxObjList  m_resizePath;

    /*
     * Popup
     */
    VfxBool     m_screenDisable;

    VfxObjList  m_popupStack;    
    VfxTimer   *m_blackScreenTimer;
    VfxWeakPtr <VfxBasePopup> m_popupLeaving;

    VfxScrTransitTypeEnum m_transitType;

    // Keep the screen rotation type, especially for inactive screen 
    VfxScrRotateTypeEnum m_rotateType;

    friend class VpiScreenProxy;
};


// Weak Pointer to VfxScreen
typedef VfxWeakPtr <VfxScreen> VfxScrWeakPtr;

#endif /* __VFX_SCREEN_H__ */

