// The VfxScreen registered name
#define VFX_SCREEN_CLASS_NAME           "Screen"

// The default screen rotate animation duration
#define VFX_SCREEN_DEFAULT_ROTATE_DUR   500

/*
 * VfxScreen::entryCanvas() flags. See VfxScreenEntryCanvasParam
 */

// Do not reset pen, key, etc.
#define VFX_SCREEN_ENTRY_CANVAS_FLAG_WITHOUT_RESET              (1 << 0)
// Forbid VRT to modify the original main base layer buffer
#define VFX_SCREEN_ENTRY_CANVAS_FLAG_FORBID_MAIN_BASE_LAYER     (1 << 1)
// Share APP context pool for VRT context pool
#define VFX_SCREEN_ENTRY_CANVAS_FLAG_SHARE_APP_CONTEXT_FOR_VRT  (1 << 2)
// Forbid VRT to use the GDI 2nd layer pool as temp memory
#define VFX_SCREEN_ENTRY_CANVAS_FLAG_FORBID_GDI_LAYER           (1 << 3)


/* 
 * VfxScreen::leaveCanvas() flags. See VfxScreenLeaveCanvasParam
 */

// Do not reset pen, key, etc.
#define VFX_SCREEN_LEAVE_CANVAS_FLAG_WITHOUT_RESET              (1 << 0)
// Forbid VRT to flatten when leaving
#define VFX_SCREEN_LEAVE_CANVAS_FLAG_FORBID_FLATTEN             (1 << 2)
// This is a hint. It tells VRT that next screen using VRT, 
#define VFX_SCREEN_LEAVE_CANVAS_FLAG_NEXT_HAS_VRT               (1 << 3)


