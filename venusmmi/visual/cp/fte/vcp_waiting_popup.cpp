/*****************************************************************************
 * Class WaitingPopup
 *****************************************************************************/

VcpWaitingPopup::VcpWaitingPopup() :
    m_waitingIcon(NULL)
{
}


void VcpWaitingPopup::onInit()
{
    VfxBasePopup::onInit();
    
    VFX_OBJ_CREATE(m_background, VfxFrame, this);
    m_background->setImgContent(VfxImageSrc(VCP_IMG_POPUP_WAITING_BACKGROUND));
    m_background->setContentPlacement(VFX_FRAME_CONTENT_PLACEMENT_TYPE_RESIZE);
    m_background->setIsCached(VFX_TRUE);
    m_background->setBounds(0, 0, VCP_WAITING_POPUP_BACKGROUND_WIDTH, VCP_WAITING_POPUP_BACKGROUND_HEIGHT);

    VFX_OBJ_CREATE(m_waitingIcon, VcpWaitingIcon, this);
    m_waitingIcon->setAnchor(0.5, 0);
    m_waitingIcon->setPos(VCP_WAITING_POPUP_BACKGROUND_WIDTH/2, VCP_WAITING_POPUP_ICON_Y);
    m_waitingIcon->setBounds(VfxRect(0, 0, VCP_WAITING_POPUP_ICON_WIDTH, VCP_WAITING_POPUP_ICON_HEIGHT));
    m_waitingIcon->setHiddenWhenStopped(VFX_FALSE);

    setBounds(0, 0, VCP_WAITING_POPUP_BACKGROUND_WIDTH, VCP_WAITING_POPUP_BACKGROUND_HEIGHT);
}


void VcpWaitingPopup::onEnter()
{
    VfxBasePopup::onEnter();
    
    m_waitingIcon->start();
}


void VcpWaitingPopup::onExit()
{
    m_waitingIcon->stop();
    
    VfxBasePopup::onExit();
}

